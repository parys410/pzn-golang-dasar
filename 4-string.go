package main

import "fmt";

func main() {
	fmt.Println("Ary" + "Setiyawan");
	fmt.Println("Ary Setiyawan");

	// Menghitung jumlah karakter di string
	fmt.Println("Ary Setiyawan","Dengan Jumlah String : ", len("Ary Setiyawan"));

	// Mengambil karakter dan output dalam byte code
	fmt.Println("Ary Setiyawan", "Karakter ke 1 : ", "Ary Setiyawan"[1]);
}